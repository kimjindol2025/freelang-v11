#!/usr/bin/env node
/**
 * extract-runtime-helpers.js
 *
 * bootstrap.js에서 런타임 헬퍼 함수들을 추출하여
 * src/runtime-helpers.ts로 변환
 *
 * 사용: node extract-runtime-helpers.js
 */

const fs = require('fs');
const path = require('path');

const bootstrapPath = path.join(__dirname, 'bootstrap.js');
const outputPath = path.join(__dirname, 'src/runtime-helpers.ts');

// bootstrap.js 읽기
const bootstrap = fs.readFileSync(bootstrapPath, 'utf8');

// 런타임 헬퍼 함수 추출 패턴
// 1. _fl_* 함수들
// 2. 기본 유틸 함수들 (char_at, substring, json_parse 등)
const patterns = [
  // _fl_로 시작하는 함수들
  /function (_fl_\w+)\([^)]*\)\s*\{\s*return[^}]*\};?/g,
  // 단순 함수들
  /function (char_at|substring|json_parse|json_stringify|js_name|js_esc|lex|parse|cond|or|and|str_repeat|map_entries)\([^)]*\)\s*\{[^}]*\};?/g,
];

const extractedFunctions = new Map();
let functionCount = 0;

// 함수 추출
function extractFunctions(text) {
  const lines = text.split('\n');
  let currentFunction = '';
  let braceCount = 0;
  let isCapturing = false;
  let functionName = '';

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];

    // 함수 시작 감지: function name(...) {
    const funcMatch = line.match(/^function\s+([a-zA-Z_$][a-zA-Z0-9_$]*)\s*\(/);
    if (funcMatch && !isCapturing) {
      functionName = funcMatch[1];
      // _fl_로 시작하거나 필수 유틸 함수인 경우만 추출
      if (functionName.startsWith('_fl_') ||
          ['char_at', 'substring', 'json_parse', 'json_stringify',
           'js_name', 'js_esc', 'lex', 'parse', 'cond', 'or', 'and',
           'str_repeat', 'map_entries', 'lazy_seq', 'lazy_take'].includes(functionName)) {
        isCapturing = true;
        currentFunction = line + '\n';
        braceCount = (line.match(/\{/g) || []).length - (line.match(/\}/g) || []).length;
        functionCount++;
      }
    } else if (isCapturing) {
      currentFunction += line + '\n';
      braceCount += (line.match(/\{/g) || []).length - (line.match(/\}/g) || []).length;

      if (braceCount === 0 && currentFunction.includes('{')) {
        extractedFunctions.set(functionName, currentFunction);
        isCapturing = false;
        functionName = '';
        currentFunction = '';
      }
    }
  }

  return extractedFunctions;
}

console.log(`📦 Extracting runtime helpers from bootstrap.js...`);
extractFunctions(bootstrap);

console.log(`✅ Found ${extractedFunctions.size} runtime helper functions`);

// TypeScript 파일 생성
const tsContent = `/**
 * src/runtime-helpers.ts
 *
 * Runtime helper functions for FreeLang
 * Extracted from bootstrap.js for self-hosting
 *
 * Generated by: node extract-runtime-helpers.js
 * Date: ${new Date().toISOString()}
 */

/**
 * 런타임 헬퍼 함수 프리앰블 생성
 * Stage1.js 최상단에 주입되어 JavaScript 코드 실행 지원
 */
export function generateRuntimePreamble(): string {
  return \`
${Array.from(extractedFunctions.values()).join('\n')}
  \`;
}

/**
 * 헬퍼 함수 목록
 */
export const HELPER_FUNCTIONS = [
${Array.from(extractedFunctions.keys()).map(name => `  '${name}',`).join('\n')}
];

export const HELPER_COUNT = ${extractedFunctions.size};
`;

// 파일 쓰기
fs.writeFileSync(outputPath, tsContent, 'utf8');

console.log(`\n✨ Generated: ${outputPath}`);
console.log(`📊 Statistics:`);
console.log(`   - Helper functions: ${extractedFunctions.size}`);
console.log(`   - Function list: ${Array.from(extractedFunctions.keys()).slice(0, 5).join(', ')}...`);
console.log(`\n📝 Next steps:`);
console.log(`   1. npm run build`);
console.log(`   2. Verify stage1.js includes runtime preamble`);
console.log(`   3. node stage1.js verify-self-host`);

process.exit(0);

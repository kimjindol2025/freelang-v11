#!/usr/bin/env node
// FreeLang v11.2: stdlib-*.ts 파일에서 함수 API 주석을 추출하여 docs/API.md 생성.
// 주석 형식: `// func_name params -> return` 다음 줄에 `"func_name": (...)` 패턴.

const fs = require("fs");
const path = require("path");

const SRC_DIR = path.resolve(__dirname, "..", "src");
const OUT = path.resolve(__dirname, "..", "docs", "API.md");

function extractModule(filePath) {
  const content = fs.readFileSync(filePath, "utf8");
  const lines = content.split("\n");
  const modName = path.basename(filePath, ".ts").replace(/^stdlib-/, "");
  const funcs = [];

  for (let i = 0; i < lines.length; i++) {
    const cur = lines[i].trim();
    // 주석 패턴: `// name args -> return` 그리고 다음 줄이 `"name": (...)` 인 경우
    const commentMatch = cur.match(/^\/\/\s*([a-z][a-z0-9_\-!?]*)\s+(.*?)\s*->\s*(.+)$/);
    if (!commentMatch) continue;
    const [, name, params, retType] = commentMatch;
    // 다음 줄 확인
    const next = (lines[i + 1] || "").trim();
    const defMatch = next.match(/^"([^"]+)"\s*:/);
    if (!defMatch || defMatch[1] !== name) continue;
    funcs.push({ name, params: params.trim(), ret: retType.trim() });
  }

  return { module: modName, funcs };
}

function render(modules) {
  const now = new Date().toISOString().slice(0, 10);
  let md = `# FreeLang v11 — stdlib API\n\n`;
  md += `> Auto-generated by \`scripts/gen-stdlib-docs.js\` (${now}). Do not edit by hand.\n\n`;
  md += `## Modules\n\n`;
  for (const m of modules) {
    if (m.funcs.length === 0) continue;
    md += `- [${m.module}](#${m.module}) (${m.funcs.length} functions)\n`;
  }
  md += `\n---\n\n`;
  for (const m of modules) {
    if (m.funcs.length === 0) continue;
    md += `## ${m.module}\n\n`;
    md += `| Function | Params | Returns |\n|----------|--------|---------|\n`;
    for (const f of m.funcs) {
      md += `| \`${f.name}\` | ${f.params} | ${f.ret} |\n`;
    }
    md += `\n`;
  }
  return md;
}

function main() {
  const files = fs.readdirSync(SRC_DIR)
    .filter((f) => f.startsWith("stdlib-") && f.endsWith(".ts"))
    .map((f) => path.join(SRC_DIR, f));

  const modules = files.map(extractModule);
  const md = render(modules);

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, md, "utf8");
  const total = modules.reduce((s, m) => s + m.funcs.length, 0);
  console.log(`✅ ${OUT} — ${modules.length} modules, ${total} functions`);
}

main();
